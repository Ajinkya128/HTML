
// Switch case is fasetr than IF else statement.
// Switch will directly Jump to the variable assigned unlike IF Else statement which goes through
// each and every iteration

var shirtsize = "L";

switch(shirtsize)
{
    case "S":
        console.log("Your shirt size is Small")
        break;
    
    case "M":
        console.log("Your shirt size is Medium")
        break;
    
    case "L":
        console.log("Your shirt size is Large")
        break;  
    
    default:
        console.log("The size you have enetered is incorrect")    
}

